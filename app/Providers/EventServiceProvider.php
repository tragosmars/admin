            SendEmailVerificationNotification::class,
        ],
        'App\Events\NoticeUser' => [//通知用户事件
            'App\Listeners\NoticeUsers',
        ],
        'App\Events\SendTalk' => [//聊天事件
            'App\Listeners\SendTalks',
        ],
    ];

    /**
     * Register any events for your application.
     *
     * @return void
     */
    public function boot()
    {
        parent::boot();

        //
    }
}
